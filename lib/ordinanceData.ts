/** 고유 시·도 목록 — 전국 목록 기준 (조례 데이터 있는 시도는 상단 표시) */
export const SIDO_LIST: string[] = Object.keys(ALL_SIGUNGU)

/** 시·도 → 시·군·구 목록 (전체 목록 반환, 조례 데이터 있는 항목 상단 표시) */
export function getSigunguList(sido: string): string[] {
  const all = ALL_SIGUNGU[sido] ?? []
  const hasData = new Set(ORDINANCE_RECORDS.filter(r => r.sido === sido).map(r => r.sigungu))
  // 조례 데이터 있는 시군구 먼저, 나머지 가나다순
  return [
    ...all.filter(s => hasData.has(s)),
    ...all.filter(s => !hasData.has(s)),
  ]
}

/** (시·도, 시·군·구) → OrdinanceRecord | undefined */
export function findRecord(sido: string, sigungu: string): OrdinanceRecord | undefined {
  return ORDINANCE_RECORDS.find(r => r.sido === sido && r.sigungu === sigungu)
}

/** 텍스트 검색 — sido 또는 sigungu에 query 포함 */
export function searchRecords(query: string): OrdinanceRecord[] {
  const q = query.trim().toLowerCase()
  if (!q) return []
  return ORDINANCE_RECORDS.filter(
    r => r.sido.toLowerCase().includes(q) || r.sigungu.toLowerCase().includes(q)
  )
}
